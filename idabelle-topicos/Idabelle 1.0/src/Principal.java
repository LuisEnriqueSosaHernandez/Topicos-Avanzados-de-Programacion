        Point punto = MouseInfo.getPointerInfo().getLocation();
        x3 = punto.x;
        y3 = punto.y;
        
        Graphics g = getGraphics();
        x2 = x3;
        y2 = y1;
        x4 = x1;
        y4 = y3;
        g.drawLine(x1, y1, x2, y2);
