    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String usr = txtUsuario.getText();
        String correo = pass.getText();
        String ip = txtIP.getText();
       if(((usr != null) && (!usr.equals("")))&&((correo != null) && (!correo.equals(""))) && ((ip!=null)&&(!ip.equals("")))){
                    try {
            
            sk = new Socket(ip, 8080);
            dos = new DataOutputStream(sk.getOutputStream());
            dis = new DataInputStream(sk.getInputStream());
            dos.writeUTF(usr);
            dos.writeUTF(correo);
            String respuesta="";
            respuesta = dis.readUTF();
            JOptionPane.showMessageDialog(null, respuesta,"Estado", JOptionPane.CANCEL_OPTION);  
            dis.close();
            dos.close();
            sk.close();
            txtUsuario.setText("");
            pass.setText("");
            txtUsuario.requestFocus();
            Login log = new Login();
            log.setVisible(true);
            this.dispose();
        } catch (IOException ex) {
            Logger.getLogger(GetPass.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        } else{
           JOptionPane.showMessageDialog(null, "Usuario y/o contrase침a inv치lido");
            txtUsuario.setText("");
            pass.setText("");
            txtUsuario.requestFocus();
       }
           
           
    }//GEN-LAST:event_jButton1MouseClicked

