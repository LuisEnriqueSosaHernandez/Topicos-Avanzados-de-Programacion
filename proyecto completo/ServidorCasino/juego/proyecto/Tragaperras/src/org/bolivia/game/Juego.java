    private void lbCreditoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_lbCreditoKeyTyped
        char c = evt.getKeyChar();
        int enter = evt.getKeyChar();
        if(c < '0' || c > '9') evt.consume();
        if(lbCredito.getText().length()>=6) evt.consume();
        if(enter == 10){
            if(lbCredito.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "No puede dejar el campo vac√≠o");
                lbCredito.requestFocus();
            }else
            lbApuesta.requestFocus();
        }
    }//GEN-LAST:event_lbCreditoKeyTyped

